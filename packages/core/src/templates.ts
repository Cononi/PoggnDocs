    "",
    "case \"$STAGE\" in",
    "  pgg-add)",
    "    [[ -f \"$TOPIC_DIR/proposal.md\" ]] || fail \"proposal.md is missing\"",
    "    ;;",
    "  pgg-plan)",
    "    [[ -f \"$TOPIC_DIR/proposal.md\" ]] || fail \"proposal.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/proposal.review.md\" ]] || fail \"reviews/proposal.review.md is missing\"",
