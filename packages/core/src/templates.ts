    "",
    "if [[ $# -ne 2 ]]; then",
    "  echo \"usage: $0 <stage> <topic|topic_dir>\" >&2",
    "  exit 1",
    "fi",
    "",
    "ROOT_DIR=\"$(cd \"$(dirname \"${BASH_SOURCE[0]}\")/../..\" && pwd)\"",
    "STAGE=\"$1\"",
    "TARGET=\"$2\"",
    "",
    "if [[ -d \"$TARGET\" ]]; then",
    "  TOPIC_DIR=\"$TARGET\"",
    "else",
    "  TOPIC_DIR=\"$ROOT_DIR/poggn/active/$TARGET\"",
    "fi",
    "",
    "fail() {",
    "  echo \"{\\\"gate\\\":\\\"fail\\\",\\\"stage\\\":\\\"$STAGE\\\",\\\"reason\\\":\\\"$1\\\"}\"",
    "  exit 1",
    "}",
    "",
    "read_proposal_value() {",
    "  local key=\"$1\"",
    "  local proposal=\"$TOPIC_DIR/proposal.md\"",
    "  [[ -f \"$proposal\" ]] || return 0",
    "  sed -n \"s/^[[:space:]]*${key}: \\\"\\([^\\\"]*\\)\\\"/\\1/p\" \"$proposal\" | head -n 1",
    "}",
    "",
    "extract_state_section() {",
    "  local title=\"$1\"",
    "  local state_file=\"$TOPIC_DIR/state/current.md\"",
    "  [[ -f \"$state_file\" ]] || return 0",
    "  awk -v title=\"$title\" '",
    "    $0 == \"## \" title { found = 1; next }",
    "    /^## / && found { exit }",
    "    found { print }",
    "  ' \"$state_file\" | sed '/^[[:space:]]*$/d'",
    "}",
    "",
    "read_audit_applicability() {",
    "  local audit_name=\"$1\"",
    "  local line",
    "  line=\"$(extract_state_section \"Audit Applicability\" | sed -n \"s/^- \\\`${audit_name}\\\`: \\\`\\([^\\\`]*\\)\\\` | \\(.*\\)$/\\1|\\2/p\" | head -n 1)\"",
    "  if [[ -z \"$line\" ]]; then",
    "    printf 'not_required|state/current.md Audit Applicability not declared\\n'",
    "    return 0",
    "  fi",
    "  printf '%s\\n' \"$line\"",
    "}",
    "",
    "require_audit_report_if_needed() {",
    "  local audit_name=\"$1\"",
    "  local report_path=\"$2\"",
    "  local applicability",
    "  applicability=\"$(read_audit_applicability \"$audit_name\")\"",
    "  local status=\"${applicability%%|*}\"",
    "  local reason=\"${applicability#*|}\"",
    "  if [[ \"$status\" == \"required\" && ! -f \"$report_path\" ]]; then",
    "    fail \"${audit_name} report is required: ${reason}\"",
    "  fi",
    "}",
    "",
    "require_current_project_metadata() {",
    "  local archive_type=\"$(read_proposal_value archive_type)\"",
    "  local project_scope=\"$(read_proposal_value project_scope)\"",
    "  [[ \"$project_scope\" == \"current-project\" ]] || fail \"project_scope must be current-project\"",
