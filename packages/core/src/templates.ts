    "FINAL_RESULT=\"$(emit_result \"$RESULT_TYPE\" \"$REASON\" \"$PUSH_STATUS\" \"$COMMIT_SHA\" \"true\" \"true\" \"$PUBLISHED_AT\" \"$PUBLISH_MODE\" \"$UPSTREAM_STATUS\" \"$CLEANUP_STATUS\" \"$CLEANUP_REASON\" \"$CLEANUP_TIMING\")\"",
