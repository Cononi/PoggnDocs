function lines(parts: string[]): string {
  return `${parts.join("\n")}\n`;
}

function numberedCoreWorkflowSkillLines(): string[] {
  return CORE_WORKFLOW_SKILLS.map((skill, index) => `${index + 1}. \`${skill}\``);
}

function optionalAuditSkillLines(): string[] {
  return OPTIONAL_AUDIT_SKILLS.map((skill) => `- \`${skill}\``);
}

function mandatoryCriteriaSection(language: TemplateLanguage): string[] {
  const title =
    language === "ko" ? "## Mandatory Implementation Criteria" : "## Mandatory Implementation Criteria";
  const criteria =
    language === "ko" ? MANDATORY_IMPLEMENTATION_CRITERIA_KO : MANDATORY_IMPLEMENTATION_CRITERIA_EN;

  return ["", title, "", ...criteria.map((criterion) => `- ${criterion}`)];
}

