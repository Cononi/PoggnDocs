function lines(parts: string[]): string {
  return `${parts.join("\n")}\n`;
}

function roleById(roleId: string): AgentRoleDefinition {
  const role = AGENT_ROLES.find((candidate) => candidate.id === roleId);
  if (!role) {
    throw new Error(`Unknown agent role: ${roleId}`);
  }
  return role;
}

function localizedRoleName(role: AgentRoleDefinition, language: TemplateLanguage): string {
  return role.displayName[language];
}

function localizedPurpose(role: AgentRoleDefinition, language: TemplateLanguage): string {
  return role.purpose[language];
}

function localizedWhenToUse(role: AgentRoleDefinition, language: TemplateLanguage): string {
  return role.whenToUse[language];
}

function flowAgentNames(route: FlowAgentRoute, language: TemplateLanguage): string {
  return route.agents.map((agentId) => localizedRoleName(roleById(agentId), language)).join(", ");
}

function flowAgentIds(route: FlowAgentRoute): string {
  return route.agents.join(",");
}

function uniqueValues(values: string[]): string[] {
  return [...new Set(values)];
}

function codexAgentRolePath(roleId: string): string {
  return `${CODEX_AGENTS_DIR}/${roleId}.toml`;
}

function tomlString(value: string): string {
  return JSON.stringify(value);
}

function tomlMultilineString(value: string): string {
  return `"""\\\n${value.replace(/"""/g, '\\"\\"\\"')}\n"""`;
}

function flowStageAliases(route: FlowAgentRoute): string[] {
  const stageAliasesByFlow: Record<string, string[]> = {
    "pgg-add": ["proposal", "add"],
    "pgg-plan": ["plan", "planning", "task"],
    "pgg-code": ["implementation", "code"],
    "pgg-refactor": ["refactor"],
    "pgg-qa": ["qa"],
    "pgg-token": ["token"],
    "pgg-performance": ["performance"]
  };

  return uniqueValues([...(stageAliasesByFlow[route.flow] ?? []), route.flow]);
}

function statePackPrimaryAgentCaseLines(): string[] {
  return FLOW_AGENT_ROUTES.flatMap((route) => [
    `  ${flowStageAliases(route).join("|")})`,
    `    PRIMARY_AGENTS="${flowAgentIds(route)}"`,
    "    ;;"
  ]);
}

