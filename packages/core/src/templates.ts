    "fi",
    "",
    "COMMIT_SHA=\"$(git -C \"$ROOT_DIR\" rev-parse HEAD)\"",
    "PUBLISHED_AT=\"$(date -u +\"%Y-%m-%dT%H:%M:%SZ\")\"",
    "PUSH_STATUS=\"success\"",
    "RESULT_TYPE=\"published\"",
    "REASON=\"Commit and push completed successfully.\"",
    "",
    "if PUSH_OUTPUT=\"$(git -C \"$ROOT_DIR\" push \"$REMOTE_NAME\" \"$BRANCH\" 2>&1)\"; then",
    "  :",
