function renderAdditionalGuidance(definition: PggSkillDefinition, language: TemplateLanguage): string[] {
  if (!definition.additionalGuidance?.length) {
    return [];
  }

  return definition.additionalGuidance.flatMap((section) =>
    renderDefinitionListSection(section.title[language], localizedMarkdown(section.body, language))
  );
}

