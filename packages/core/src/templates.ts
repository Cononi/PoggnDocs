    "    ;;",
    "  pgg-code)",
    "    [[ -f \"$TOPIC_DIR/proposal.md\" ]] || fail \"proposal.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/plan.md\" ]] || fail \"plan.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/task.md\" ]] || fail \"task.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/plan.review.md\" ]] || fail \"reviews/plan.review.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/task.review.md\" ]] || fail \"reviews/task.review.md is missing\"",
    "    require_current_project_metadata",
    "    find \"$TOPIC_DIR/spec\" -type f -name '*.md' | grep -q . || fail \"spec files are missing\"",
    "    ;;",
    "  pgg-refactor)",
    "    [[ -f \"$TOPIC_DIR/implementation/index.md\" ]] || fail \"implementation/index.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/code.review.md\" ]] || fail \"reviews/code.review.md is missing\"",
    "    require_current_project_metadata",
    "    ;;",
    "  pgg-token)",
    "    [[ -f \"$TOPIC_DIR/implementation/index.md\" ]] || fail \"implementation/index.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/code.review.md\" ]] || fail \"reviews/code.review.md is missing\"",
    "    [[ -f \"$TOPIC_DIR/reviews/refactor.review.md\" ]] || fail \"reviews/refactor.review.md is missing\"",
